    private void passgenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_passgenActionPerformed

        String firstCharPool = "ABCDEFGHIJKLMNOPQRSTUVWXYZ";
        String digitPool = "0123456789";

        // Create a random object
        Random random = new Random();

        // Generate the first character (a letter)
        int firstCharIndex = random.nextInt(firstCharPool.length());
        char firstChar = firstCharPool.charAt(firstCharIndex);

        // Generate the remaining seven digits
        StringBuilder remainingDigits = new StringBuilder();
        int remainingDigitsLength = 7; // Adjust the length as needed
        for (int i = 0; i < remainingDigitsLength; i++) {
            int digitIndex = random.nextInt(digitPool.length());
            char randomDigit = digitPool.charAt(digitIndex);
            remainingDigits.append(randomDigit);
        }

        String passportNumber = firstChar + remainingDigits.toString();
        pno.setText(passportNumber);

    }//GEN-LAST:event_passgenActionPerformed

    private void pnoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_pnoFocusLost
      
         try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Scanner scanner = new Scanner(System.in);
            Connection con = DriverManager.getConnection(
                    "jdbc:mysql://localhost:3306/ams", "root", "admin");
            String passno = pno.getText();
            String sql = "SELECT * FROM managepassenger WHERE passportno = '" + passno + "' ";
            PreparedStatement pst = con.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                fname.setText(rs.getString("fullname"));
                SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
                java.util.Date utilDate = sdf.parse(rs.getString("dateofbirth"));
                java.sql.Date sqlDate = new java.sql.Date(utilDate.getTime());
                dob.setDate(sqlDate);
                
                String genderValue = rs.getString("gender");
                if ("male".equals(genderValue)) {
                    male.setSelected(true);
                    female.setSelected(false);
                } else {
                    male.setSelected(false);
                    female.setSelected(true);
                }
                
                cno.setText(rs.getString("contactno"));
    
                
            }
              con.close();
         }
          
            catch (Exception e)
                     {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
        
        
        
        
    }//GEN-LAST:event_pnoFocusLost

