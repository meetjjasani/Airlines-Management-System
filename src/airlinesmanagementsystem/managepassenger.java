    private void SEARCHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SEARCHActionPerformed

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");

            Connection con = DriverManager.getConnection(
                    "jdbc:mysql://localhost:3306/ams", "root", "admin");
            Statement st = con.createStatement();
            String sql = "select * from managepassenger";
            PreparedStatement pst = con.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
            dt.setRowCount(0);

            while (rs.next()) {
                Object o[] = {rs.getString("fullname"), rs.getString("dateofbirth"), rs.getString("gender"), rs.getString("contactno"), rs.getString("passportno")};
                dt.addRow(o);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_SEARCHActionPerformed

