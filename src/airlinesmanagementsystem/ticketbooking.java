    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
      
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");

            Connection con = DriverManager.getConnection(
                    "jdbc:mysql://localhost:3306/ams", "root", "admin");
            Statement st = con.createStatement();
            String sql = "select * from ticketbooking";
            PreparedStatement pst = con.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
            dt.setRowCount(0);

            while (rs.next()) {
                Object o[] = {rs.getString("ticketid"), rs.getString("passengername"), rs.getString("flightcode"), rs.getString("passportno"), rs.getString("nationality"), rs.getString("amount")};
                dt.addRow(o);
            }

        } catch (Exception e)
        {
            e.printStackTrace();
        }
        
        
        
    }//GEN-LAST:event_searchActionPerformed

