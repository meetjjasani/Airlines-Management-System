    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");

            Connection con = DriverManager.getConnection(
                    "jdbc:mysql://localhost:3306/ams", "root", "admin");

            String query = "insert into ticketbooking values (?,?,?,?,?,?)";
            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1, passengerid.getText());
            pst.setString(2, passengername.getText());
            pst.setString(3, flightcode.getText());
            pst.setString(4, passportno.getText());
            pst.setString(5, nationality.getText());
            pst.setString(6, amount.getText());

            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data Inserted Successfully");

            con.close();

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, e);

        }


    }//GEN-LAST:event_jButton1ActionPerformed

